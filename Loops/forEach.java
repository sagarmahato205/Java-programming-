public class forEach{
    public static void main(String[] args) {
        int[] arr = {2,4,5,6,7};
        for(int n:arr){
            System.out.println(n);
        }
        String[] name = {"ram","mohan","sagar","rohit"};
        for(String Na:name){
            System.out.println(Na);
        }
    }
}